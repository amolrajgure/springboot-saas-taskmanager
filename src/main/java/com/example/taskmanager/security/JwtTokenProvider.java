package com.example.taskmanager.security;

import io.jsonwebtoken.*;
import io.jsonwebtoken.security.Keys;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

import java.security.Key;
import java.util.Date;

/**
 * Generate JWT tokens with user info and expiration
 * Validate tokens in incoming requests
 * Extract username from tokens
 * | Part                       | Purpose                                      |
 * | -------------------------- | -------------------------------------------- |
 * | `@Value("${jwt.secret}")`  | Reads secret key from application.properties |
 * | `createToken()`            | Creates JWT with username & expiration       |
 * | `validateToken()`          | Checks signature & expiry                    |
 * | `getUsername()`            | Extracts username from valid token           |
 * | Uses `io.jsonwebtoken` lib | For compact JWT building/parsing             |
 */

@Component
public class JwtTokenProvider {

    private final Key key;

    private final long validityInMilliseconds;

    public JwtTokenProvider(
            @Value("${jwt.secret}") String secret,
            @Value("${jwt.expiration}") long validityInMilliseconds) {
        this.key = Keys.hmacShaKeyFor(secret.getBytes());
        this.validityInMilliseconds = validityInMilliseconds;
    }

    public String createToken(String username) {
        Date now = new Date();
        Date expiry = new Date(now.getTime() + validityInMilliseconds);

        return Jwts.builder()
                .setSubject(username)
                .setIssuedAt(now)
                .setExpiration(expiry)
                .signWith(key, SignatureAlgorithm.HS256)
                .compact();
    }

    public boolean validateToken(String token) {
        try {
            Jwts.parserBuilder()
                    .setSigningKey(key)
                    .build()
                    .parseClaimsJws(token);
            return true;
        } catch (JwtException | IllegalArgumentException ex) {
            return false;
        }
    }

    public String getUsername(String token) {
        return Jwts.parserBuilder()
                .setSigningKey(key)
                .build()
                .parseClaimsJws(token)
                .getBody()
                .getSubject();
    }
}
